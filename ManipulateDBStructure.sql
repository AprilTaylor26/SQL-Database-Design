-- SECTION 2: Create CHATLOG Table
-- ==================================================

CREATE TABLE CHATLOG (
    CHATID NUMBER(3),
    RECEIVERID NUMBER(3),
    SENDERID NUMBER(3),
    DATESENT DATE,
    CONTENT VARCHAR2(250),

    CONSTRAINT PK_CHATLOG
        PRIMARY KEY (CHATID),

    CONSTRAINT FK_CHATLOG_RECEIVERID
        FOREIGN KEY (RECEIVERID)
        REFERENCES USERBASE(USERID),

    CONSTRAINT FK_CHATLOG_SENDERID
        FOREIGN KEY (SENDERID)
        REFERENCES USERBASE(USERID)
);

