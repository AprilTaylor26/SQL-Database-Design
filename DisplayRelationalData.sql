FROM USERBASE
LEFT JOIN INFRACTIONS ON USERBASE.USERID = INFRACTIONS.USERID
WHERE BIRTHDAY <= ADD_MONTHS(SYSDATE, -216) AND DATEASSIGNED <= ADD_MONTHS(SYSDATE, -4);

-- QUESTION 21 --
SELECT A.USERNAME, B.DATEASSIGNED, C.RULENUM || '.  ' || C.TITLE AS FULL_GUIDELINE
FROM INFRACTIONS B
JOIN USERBASE A ON B.USERID = A.USERID
JOIN COMMUNITYRULES C ON B.RULENUM = C.RULENUM
ORDER BY A.USERNAME, B.DATEASSIGNED;

-- QUESTION 22 --
SELECT A.USERID, A.USERNAME, A.EMAIL, SUM(C.SEVERITYPOINT) AS TOTAL_SEVERITY_POINTS
FROM USERBASE A
JOIN INFRACTIONS B ON A.USERID = B.USERID
JOIN COMMUNITYRULES C ON B.RULENUM = C.RULENUM
GROUP BY A.USERID, A.USERNAME, A.EMAIL
ORDER BY A.USERID, A.USERNAME, A.EMAIL;

