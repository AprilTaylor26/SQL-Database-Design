-- QUESTION 9 --
CREATE OR REPLACE VIEW RATING_VIEW AS
SELECT PRODUCTNAME, GENRE, RATING
FROM PRODUCTLIST
JOIN REVIEWS ON PRODUCTLIST.PRODUCTCODE = REVIEWS.PRODUCTCODE
WHERE RATING IN (1, 5)
ORDER BY RATING ASC;

-- QUESTION 10 --
SELECT COUNT(ORDERID) AS PRODUCTS_ORDERED
FROM PRODUCTLIST
JOIN ORDERS ON PRODUCTLIST.PRODUCTCODE = ORDERS.PRODUCTCODE
GROUP BY GENRE
ORDER BY GENRE ASC;

-- QUESTION 11 --
CREATE OR REPLACE VIEW INFO_ON_PRODUCTS AS
SELECT PUBLISHER, AVG(PRICE) AS AVERAGE_PRICE, SUM(HOURSPLAYED) AS TOTAL_HOURS_PLAYED
FROM PRODUCTLIST
JOIN USERLIBRARY ON PRODUCTLIST.PRODUCTCODE = USERLIBRARY.PRODUCTCODE
GROUP BY PUBLISHER
ORDER BY PUBLISHER ASC;

-- QUESTION 12 --
SELECT B.PUBLISHER, SUM(A.PRICE) AS TOTAL_SPENT
FROM ORDERS A
JOIN PRODUCTLIST B ON A.PRODUCTCODE = B.PRODUCTCODE
GROUP BY B.PUBLISHER
ORDER BY TOTAL_SPENT DESC;

